#include <bits/stdc++.h>> 
//모든 C++ 표준 라이브러리가 포함된 헤더파일
//헤더파일 include 시키다.(프로그램에 포함 시키다)
using namespace std; 
//std라는 네임 스페이스를 사용
//네임 스페이스 : 변수명에 범위 걸어 놓는 것 
string a;
//문자열 변수 a 선언 
//<타입> <변수이름>
int main() {
  cin >> a; //변수 a 입력 받음
  cout << a << "\n"; //변수 a를 출력
  return 0; //main 함수를 종료시키는 return 0 
  //프로세스 정상적 마무리 의미 (process exit call success)
  //cpp파일 하나당 하나의 main함수만 만들 수 있음 
}
