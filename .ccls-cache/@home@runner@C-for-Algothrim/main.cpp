#include <bits/stdc++.h>> ///모든 표준 라이브러리가 포함된 헤더파일
// include 지시문 활용, 프로그램에 포함 시킴
using namespace std; 
// std라는 네임스페이스(변수명 중복 방지를 위해 변수명에 범위를 걸어 놓는 것)를 사용
typedef int i;
// int라는 타입을 i라는 새로운 별칭으로 바꿔서 표현
// typedef <타입> <별칭>
//타입 이름 새롭게 별칭으로 정의, 이름 대신 별칭으로 사용 가능
int main() {
  i a = 1; 
  cout << a << '\n'; //변수 a출력
  return 0; //main함수 종료시키는 return 0 
}
