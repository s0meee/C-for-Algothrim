#include <bits/stdc++.h>
//C++의 모든 표준라이브러리가 포함된 헤더파일을 include 시킨다.(프로그램에 포함 시킨다)
using namespace std;
//std라는 namespace를 사용
//네임스페이스는 변수명 중복 방지를 위해 변수명에 범위를 걸어 놓는 것
string a; 
//string이라는 타입을 가진 a라는 변수
int main(){
  cin >> a; 
  //변수 a를 입력 받음
  cout << a << "\n"; 
  //변수 a를 출력함
  return 0; 
  //main함수 종료
  //프로세스 정상적 마무리
}
